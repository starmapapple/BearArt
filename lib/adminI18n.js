export function normalizeAdminLocale(value) {
  return ADMIN_LOCALES.includes(value) ? value : "zh";
}

export function translateAdmin(locale, text) {
  return normalizeAdminLocale(locale) === "id" ? id[text] || text : text;
}

export function adminDateLocale(locale) {
  return normalizeAdminLocale(locale) === "id" ? "id-ID" : "zh-CN";
}
